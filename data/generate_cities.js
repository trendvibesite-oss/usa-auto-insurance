// Deduplicate: keep first ZIP for each city+state combo
const seen = new Set();
const unique = [];
for (const entry of rawData) {
  const key = `${entry.city}|${entry.abbr}`;
  if (!seen.has(key)) {
    seen.add(key);
    unique.push(entry);
  }
}

// Generate slug
function makeSlug(city, abbr) {
  // Special case: Ohio Bowling Green
  if (city === "Bowling Green" && abbr === "OH") {
    return "bowling-green-oh";
  }
  return city.toLowerCase().replace(/\s+/g, '-') + '-' + abbr.toLowerCase();
}

// Build final entries
const cities = unique.map(e => ({
  name: e.city,
  state: e.state,
  stateAbbr: e.abbr,
  slug: makeSlug(e.city, e.abbr),
  zip: e.zip
}));

// Sort: KY first, then OH; within each state sort by city name alphabetically
const stateOrder = { KY: 0, OH: 1 };
cities.sort((a, b) => {
  if (stateOrder[a.stateAbbr] !== stateOrder[b.stateAbbr]) {
    return stateOrder[a.stateAbbr] - stateOrder[b.stateAbbr];
  }
  return a.name.localeCompare(b.name);
});

